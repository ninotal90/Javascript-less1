// if statement

let number = 100;

if (number < 50) {
  console.log("<50");
} else if (number > 20) {
  console.log(">20");
} else {
  console.log("error");
}

let userName = "mariam";

if ((userName = "mariam")) {
  console.log("true");
} else {
  console.log(false);
}

// swich statement

switch (userName) {
  case userName = "mariam":
    console.log("true");
    break;
    default:
        console.log(false)
}
